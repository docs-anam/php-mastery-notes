/*
MVC stands for Model-View-Controller, a design pattern used to separate application logic.

- Model: Handles data and business logic (e.g., database operations).
- View: Manages the presentation layer (HTML, CSS, output).
- Controller: Acts as an intermediary, processing user input and updating Model/View.

History:
MVC was first introduced in the late 1970s by Trygve Reenskaug for Smalltalk-76. It became popular for organizing code in desktop and web applications, and is now widely used in PHP frameworks.

Diagram:
    +------------+        +------------+        +------------+
    |   Model    | <----> | Controller | <----> |   View     |
    +------------+        +------------+        +------------+
         ^                     |                    |
         |---------------------+--------------------|

Benefits:
- Separation of concerns: Easier maintenance and testing.
- Reusability: Components can be reused across the application.
- Scalability: Facilitates larger, more complex applications.

In PHP, MVC frameworks like Laravel, Symfony, and CodeIgniter help structure code using this pattern.
*/
