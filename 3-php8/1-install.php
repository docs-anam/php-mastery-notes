// Summary: How to install PHP 8.0

/*
1. On Windows:
    - Download PHP 8.0 from https://windows.php.net/download/
    - Extract the files to a folder (e.g., C:\php8)
    - Add the folder to your system PATH
    - Configure php.ini as needed

2. On macOS (using Homebrew):
    - Open Terminal
    - Run: brew install php@8.0
    - Link PHP 8.0: brew link --overwrite --force php@8.0

3. On Ubuntu/Debian:
    - sudo apt update
    - sudo apt install software-properties-common
    - sudo add-apt-repository ppa:ondrej/php
    - sudo apt update
    - sudo apt install php8.0

4. Verify installation:
    - Run: php -v
    - Output should show PHP 8.0.x

5. For MAMP/XAMPP:
    - Download the PHP 8.0 package compatible with your stack
    - Replace or add the PHP folder in your MAMP/XAMPP installation
    - Update configuration to use PHP 8.0

Refer to the official documentation for more details: https://www.php.net/manual/en/install.php
*/
