<?php
// Arithmetic Operators in PHP

/*
Arithmetic operators are used to perform common mathematical operations.
Here are the main arithmetic operators in PHP:

1. Addition (+)
    Adds two values.
    Example: $a + $b

2. Subtraction (-)
    Subtracts the right operand from the left.
    Example: $a - $b

3. Multiplication (*)
    Multiplies two values.
    Example: $a * $b

4. Division (/)
    Divides the left operand by the right.
    Example: $a / $b

5. Modulus (%)
    Returns the remainder of division.
    Example: $a % $b

6. Exponentiation (**)
    Raises the left operand to the power of the right.
    Example: $a ** $b
*/

// Example usage:
$a = 10;
$b = 3;

echo "Addition: " . ($a + $b) . "\n";         // 13
echo "Subtraction: " . ($a - $b) . "\n";      // 7
echo "Multiplication: " . ($a * $b) . "\n";   // 30
echo "Division: " . ($a / $b) . "\n";         // 3.333...
echo "Modulus: " . ($a % $b) . "\n";          // 1
echo "Exponentiation: " . ($a ** $b) . "\n";  // 1000
?>